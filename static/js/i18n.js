function translate(text) {
    const currentLang = getCurrentLanguage();
    return translations[currentLang] && translations[currentLang][text] ? translations[currentLang][text] : text;
}

function getCurrentLanguage() {
    return document.documentElement.lang || 'ru';
}

function setLanguage(lang) {
    document.documentElement.lang = lang;
    document.cookie = `django_language=${lang}; path=/; max-age=31536000`;
    
    // Обновляем все тексты на странице
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        element.textContent = translate(key);
    });
    
    // Обновляем все плейсхолдеры
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = translate(key);
    });
    
    // Обновляем все значения
    document.querySelectorAll('[data-i18n-value]').forEach(element => {
        const key = element.getAttribute('data-i18n-value');
        element.value = translate(key);
    });
    
    // Обновляем все заголовки
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        element.title = translate(key);
    });
    
    // Обновляем текст кнопки языка
    const langButton = document.querySelector('.lang-current');
    if (langButton) {
        langButton.innerHTML = lang === 'ru' ? '🇷🇺 Русский ▼' : '🇬🇧 English ▼';
    }
    
    // Обновляем активный класс в выпадающем списке
    document.querySelectorAll('.lang-option').forEach(option => {
        if (option.value === lang) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });
}

// Инициализация при загрузке страницы
document.addEventListener('DOMContentLoaded', function() {
    // Получаем язык из cookie
    const lang = document.cookie.replace(/(?:(?:^|.*;\s*)django_language\s*\=\s*([^;]*).*$)|^.*$/, "$1") || 'ru';
    setLanguage(lang);
    
    // Добавляем обработчики для кнопок смены языка
    document.querySelectorAll('.lang-option').forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.value;
            setLanguage(lang);
            
            // Отправляем запрос на сервер для сохранения в сессии
            fetch('/set-language/', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/x-www-form-urlencoded',
                    'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]')?.value || '{{ csrf_token }}'
                },
                body: `language=${lang}&next=${window.location.pathname}`
            });
        });
    });
});